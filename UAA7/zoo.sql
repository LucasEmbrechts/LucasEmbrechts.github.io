-- Table des espèces
CREATE TABLE Espece (
    nomEspece VARCHAR(50) PRIMARY KEY,
    description VARCHAR(255)
);

-- Table des animaux
CREATE TABLE Animal (
    numPuce INT PRIMARY KEY,
    nomAnimal VARCHAR(50) NOT NULL,
    poids DECIMAL(8,2) NOT NULL CHECK (poids >= 0),
    sexe VARCHAR(1) NOT NULL CHECK (sexe IN ('M', 'F')),
    dateNaissance DATE CHECK (dateNaissance <= CURRENT_DATE),
    nomEspece VARCHAR(50) NOT NULL,
    FOREIGN KEY (nomEspece) REFERENCES Espece(nomEspece),
    UNIQUE (nomAnimal, nomEspece)
);

-- Table des soigneurs
CREATE TABLE Soigneur (
    nomSoigneur VARCHAR(50) PRIMARY KEY,
    specialite VARCHAR(100) NOT NULL CHECK (specialite IN ('Vétérinaire', 'Nutritionniste', 'Dentiste', 'Comportementaliste'))
);

-- Table des soins
CREATE TABLE Soin (
    numPuceAnimal INT,
    nomSoigneur VARCHAR(50),
    dateSoin DATE,
    description VARCHAR(255) NOT NULL,
    cout DECIMAL(8,2) NOT NULL CHECK (cout >= 0),
    FOREIGN KEY (numPuceAnimal) REFERENCES Animal(numPuce),
    FOREIGN KEY (nomSoigneur) REFERENCES Soigneur(nomSoigneur),
    PRIMARY KEY (numPuceAnimal, nomSoigneur, dateSoin)
);

-- Table des aliments
CREATE TABLE Aliment (
    codeBarreAliment INT PRIMARY KEY,
    nomAliment VARCHAR(50) NOT NULL,
    coutUnitaire DECIMAL(8,2) NOT NULL CHECK (coutUnitaire >= 0)
);



-- Table des régimes alimentaires
CREATE TABLE Regime (
    numPuceAnimal INT,
    aliment INT,
    quantite DECIMAL(5,2) CHECK (quantite >= 0 AND quantite <= 1000),
    FOREIGN KEY (numPuceAnimal) REFERENCES Animal(numPuce),
    FOREIGN KEY (aliment) REFERENCES Aliment(codeBarreAliment),
    PRIMARY KEY (numPuceAnimal, aliment)
);


